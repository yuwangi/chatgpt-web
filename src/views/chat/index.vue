  )
  scrollToBottom()

  loading.value = true
  prompt.value = ''

  let options: Chat.ConversationRequest = {}
  // const lastContact = conversationList.value[conversationList.value.length - 1]?.text || ''
  const lastContext = conversationList.value[conversationList.value.length - 1]?.conversationOptions
  // console.log('lastContext_____________', lastContact)
  if (lastContext && usingContext.value && isChageModel.value === false)
    options = { ...lastContext }

  isChageModel.value = false

  addChat(
    +uuid,
    {
      dateTime: new Date().toLocaleString(),
      text: '',
      cost: 0,
      loading: true,
      inversion: false,
      error: false,
      conversationOptions: null,
      requestOptions: { prompt: message, options: { ...options } },
    },
  )
  scrollToBottom()

  try {
    let lastText = ''
    const fetchChatAPIOnce = async () => {
      if (searchType.value === 'ChatGPT') {
        await fetchChatAPIProcess<Chat.ConversationResponse>({
          prompt: message,
          options,
          model: curModel.value,
          signal: controller.signal,
          onDownloadProgress: ({ event }) => {
            const xhr = event.target
            const { responseText } = xhr
            // Always process the final line
            const lastIndex = responseText.lastIndexOf('\n')
            let chunk = responseText
            if (lastIndex !== -1)
              chunk = responseText.substring(lastIndex)
            try {
              const data = JSON.parse(chunk)
              // lastContact.length +
              const priceRateMessage = curModel.value === 'gpt-3.5' ? 10 : 150
              const priceRateResponse = curModel.value === 'gpt-3.5' ? 10 : 300
              const length: number = message.length * priceRateMessage + (data.text || '').length * priceRateResponse

              const cost = Number(Number(length * 1 * 0.000014).toFixed(6))

              costTotal = cost
              updateChat(
                +uuid,
                dataSources.value.length - 1,
                {
                  dateTime: new Date().toLocaleString(),
                  text: lastText + data.text ?? '',
                  inversion: false,
                  error: false,
                  loading: false,
                  conversationOptions: { conversationId: data.conversationId, parentMessageId: data.id },
                  requestOptions: { prompt: message, options: { ...options } },
                },
              )

              if (openLongReply && data.detail.choices[0].finish_reason === 'length') {
                options.parentMessageId = data.id
                lastText = data.text
                message = ''
                return fetchChatAPIOnce()
              }

              scrollToBottomIfAtBottom()
            }
            catch (error) {
              //
            }
          },
        })
      }
      else {
        await fetchBingAPIProcess({
          prompt: message,
          options,
          signal: controller.signal,
          onDownloadProgress: ({ event }) => {
            const xhr = event.target
            const { responseText } = xhr
            // console.log('responseText----------', responseText)
            // Always process the final line
            const lastIndex = responseText.lastIndexOf('\n')
            let chunk = responseText
            if (lastIndex !== -1)
              chunk = responseText.substring(lastIndex)
            try {
              const data = JSON.parse(chunk)
              // const length: number = lastContact.length + message.length + (data.text || '').length
              let text = lastText + data.text ?? ''

              // console.log('bingData>>>>>>>>>', data)
              if (data.conversationId) {
                const list = data.details.sourceAttributions || []
                if (list && list.length > 0)
                  text += '\n\n\n ##### 了解详细信息:\n\n'

                list.forEach((element: any, index: number) => {
                  text += `${(index + 1)}.[${element.providerDisplayName}](${element.seeMoreUrl})\n`
                })
              }

              const cost = 0

              costTotal = cost
              updateChat(
                +uuid,
                dataSources.value.length - 1,
                {
                  dateTime: new Date().toLocaleString(),
                  text,
                  inversion: false,
                  error: false,
                  loading: false,
                  conversationOptions: { parentMessageId: data?.messageId, jailbreakConversationId: data?.jailbreakConversationId, fromTo: 'bing' },
                  requestOptions: { prompt: message, options: { ...options } },
                },
              )

              if (openLongReply && data.detail.choices[0].finish_reason === 'length') {
                options.parentMessageId = data.id
                lastText = data.text
                message = ''
                return fetchChatAPIOnce()
              }

              scrollToBottom()
            }
            catch (error) {
              //
            }
          },
        })
      }
    }

    await fetchChatAPIOnce()
  }
  catch (error: any) {
    console.error(error?.message)
    const errorMessage = error?.message ?? t('common.wrong')

    if (error.message === 'canceled') {
      updateChatSome(
        +uuid,
        dataSources.value.length - 1,
        {
          loading: false,
        },
      )
      scrollToBottomIfAtBottom()
      return
    }

    const currentChat = getChatByUuidAndIndex(+uuid, dataSources.value.length - 1)

    if (currentChat?.text && currentChat.text !== '') {
      updateChatSome(
        +uuid,
        dataSources.value.length - 1,
        {
          text: `${currentChat.text}\n[${errorMessage}]`,
          cost: 0,
          error: false,
          loading: false,
        },
      )
      return
    }

    updateChat(
      +uuid,
      dataSources.value.length - 1,
      {
        dateTime: new Date().toLocaleString(),
        text: errorMessage,
        cost: 0,
        inversion: false,
        error: true,
        loading: false,
        conversationOptions: null,
        requestOptions: { prompt: message, options: { ...options } },
      },
    )
    scrollToBottomIfAtBottom()
  }
  finally {
    loading.value = false

    console.error('costTotal', costTotal)

    if (costTotal) {
      deduction({ balance: costTotal }).then((res) => {
        console.error('res', res.data)
        // if (!res.data) {

        // }
      }).catch((err) => {
        console.error('err', err)
      })
    }
  }
}

